// Color mapping based on player level/age
window.getPlayerColors = function(level) {
    // Base colors
    const colors = {
        1: '#000000', // Outline
        2: '#FFE0BD', // Skin
        3: '#FF9999', // Mouth
        4: '#4444FF', // Clothes
        5: '#444444'  // Pants/Shoes
    };
    
    // Modify colors based on level/age
    if (level < 5) { // Young
        colors[4] = '#88FF88'; // Light green clothes
    } else if (level < 10) { // Adult
        colors[4] = '#4444FF'; // Blue clothes
    } else if (level < 15) { // Middle aged
        colors[1] = '#666666'; // Graying hair
    } else { // Elder
        colors[1] = '#CCCCCC'; // White hair
        colors[4] = '#884444'; // Darker clothes
    }
    
    return colors;
};

// Animation system
window.spriteAnimation = {
    frame: 0,
    frameCount: 2,
    frameDelay: 8,
    tickCount: 0,
    
    update() {
        this.tickCount++;
        if (this.tickCount >= this.frameDelay) {
            this.frame = (this.frame + 1) % this.frameCount;
            this.tickCount = 0;
        }
    },
    
    getCurrentFrame(direction, isMoving) {
        // Always return the base sprite for the current direction
        return window.sprites[direction] || window.sprites.down;
    }
};
